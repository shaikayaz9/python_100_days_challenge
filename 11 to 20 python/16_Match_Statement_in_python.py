# match statement exatly looks like (java & javascript ) switch statement

x = int(input("Enter number 0 to 3 : "))

match x :
    case 0:
     print("good morning")
    case 1:
      print("Good after noon")
    case 2:
     print("good evening")
    case 3:
     print("good night")
    case _:
     print("not valid input")


    
